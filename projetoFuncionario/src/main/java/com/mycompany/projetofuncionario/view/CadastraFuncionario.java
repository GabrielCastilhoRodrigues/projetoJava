    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        if (nomeFuncionario.getText().trim() != null
                && dataAniversarioFuncionario.getText().trim() != null
                && salarioFuncionario.getText().trim() != null
                && funcaoFuncionario.getText().trim() != null) {
            DateTimeFormatter formatData
                    = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            LocalDate dataAniversario
                    = LocalDate.parse(dataAniversarioFuncionario.getText(),
                            formatData);

            BigDecimal salario
                    = new BigDecimal(salarioFuncionario.getText()
                            .replace(".", "").replace(",", "."));

            OperacoesFuncionario operacoesFuncionario
                    = new OperacoesFuncionario();

            if (funcionarioParaEdicao == null) {
                Funcionario funcionario
                        = operacoesFuncionario.insereFuncionario(
                                nomeFuncionario.getText(),
                                dataAniversario,
                                salario,
                                funcaoFuncionario.getText());

                principal.listaFuncionarios.add(funcionario);
            } else {
                operacoesFuncionario.atualizaFuncionario(funcionarioParaEdicao,
                        nomeFuncionario.getText(),
                        dataAniversario,
                        salario,
                        funcaoFuncionario.getText());
            }

            principal.LoadTabelaFuncionario(posicaoCombo);
            fechaTela();
        } else {
            JOptionPane.showMessageDialog(this,
                    textosDeTela.validacaoCamposFuncionario);
        }
    }//GEN-LAST:event_btnOkActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        fechaTela();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void fechaTela() {
        this.dispose();
        principal.setVisible(true);
    }

    private void insereDadosFuncionarioParaEdicao(Funcionario funcionario) {
        String format = "#,##0.00";
        NumberFormat formatSalario = new DecimalFormat(format);
        String salario = formatSalario.format(
                            funcionario.getSalario().doubleValue());

        nomeFuncionario.setText(funcionario.getNome());
        dataAniversarioFuncionario.setText(funcionario.getDataNascimento()
                .format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        salarioFuncionario.setText(salario);
        funcaoFuncionario.setText(funcionario.getFuncao());
    }

